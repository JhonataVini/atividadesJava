
public class Pessoa {

	private String nome;
	private double peso;
	private double altura;
	
	
	
	void setNome(String s){	
		nome = s;	
	}
    String getNome(){

		return nome;	
	}
    
    
	void setPeso(double p){	
		peso = p;	
	}
	
    double getPeso(){

		return peso;	
	}
    
    
	public double getAltura() {
		return altura;
	}
	public void setAltura(double altura) {
		this.altura = altura;
	}
    
	
}
